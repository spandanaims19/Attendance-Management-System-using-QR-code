    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        
        try
        {
            String email=textEmail.getText();
            String name=textName.getText();
            String contact=textContact.getText();
            String address=textAddress.getText();
            String state=textState.getText();
            String city=textCity.getText();
            
            String emailRegex="^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
            
            if(!email.matches(emailRegex))
            {
                JOptionPane.showMessageDialog(null, "Invalid Email", "Invalid", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String contactRegex="^\\d{10}$";
            
            if(!contact.matches(contactRegex))
            {
                JOptionPane.showMessageDialog(null, "Invalid Contact Number", "Invalid", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if(name.isEmpty() || email.isEmpty() || contact.isEmpty() || address.isEmpty() || state.isEmpty() || city.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "One or fields are empty", "Field Empty", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            Connection c=ConnectionProvider.getConn();
            Statement statement=c.createStatement();
            
            try
            {
                ResultSet resultSet=statement.executeQuery("SELECT * FROM userdetails WHERE email='"+email+"'");
                
                if(!resultSet.next())
                {
                    JOptionPane.showMessageDialog(null, "Duplicate Email", "Duplicate", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            catch(Exception e)
            {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
            
            String imageName=saveImage(email);
            String updateQuery;
            if(imageName!=null)
            {
                updateQuery="UPDATE userdetails SET  name=?, contact=?, address=?, state=?, city=?, imagename=? WHERE regid=?";
            }
            else
            {
                updateQuery="UPDATE userdetails SET name=?, contact=?, address=?, state=?, city=? WHERE regid=?";
            }
            
            PreparedStatement preparedStatement=c.prepareStatement(updateQuery);
            preparedStatement.setString(1, name);
            preparedStatement.setString(2, contact);
            preparedStatement.setString(3, address);
            preparedStatement.setString(4, state);
            preparedStatement.setString(5, city);
            
            if(imageName!=null)
            {
                preparedStatement.setString(6, imageName);
                preparedStatement.setString(7, regid);
            }
            else
            {
                preparedStatement.setString(7, regid);
            }
            
            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(null, "User Updated Successfully", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
            clear();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private String saveImage(String email)
    {
        if(originalImage!=null && selectedFile!=null)
        {
            try
            {
                String savePath=BDUtility.getPath("images");
                String extension=BDUtility.getFileExtension(selectedFile.getName());
                String imageName=email+"."+extension;
                File saveFile=new File(savePath+imageName);
                
                BufferedImage scaledImage=BDUtility.scaleImage(originalImage, ImageIO.read(selectedFile));
                ImageIO.write(scaledImage, extension, saveFile);
                return imageName;
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
        return "";
    }
    
    
