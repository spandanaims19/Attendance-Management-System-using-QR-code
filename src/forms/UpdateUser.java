    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String email=textEmail.getText();
        String emailRegex="^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
        
        if(!email.matches(emailRegex))
        {
            JOptionPane.showMessageDialog(null, "Invalid Email", "Invalid", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try
        {
            Connection c=ConnectionProvider.getConn();
            Statement statement=c.createStatement();
            ResultSet resultSet=statement.executeQuery("SELECT * FROM userdetails WHERE email='"+email+"'");
            
            if(resultSet.next())
            {
                textName.setText(resultSet.getString("name"));
                textContact.setText(resultSet.getString("contact"));
                textAddress.setText(resultSet.getString("address"));
                textState.setText(resultSet.getString("state"));
                textCity.setText(resultSet.getString("city"));
                regid=resultSet.getString("regid");
                String imageName=resultSet.getString("imagename");
                
                existingImageName=Objects.isNull(imageName) || imageName.isEmpty()?null:imageName;
                
                if(!Objects.isNull(existingImageName))
                {
                    String imagePath=BDUtility.getPath("images")+existingImageName;
                    File imageFile=new File(imagePath);
                    
                    if(imageFile.exists())
                    {
                        ImageIcon icon=new ImageIcon(imagePath);
                        Image image=icon.getImage().getScaledInstance(322, 286, Image.SCALE_SMOOTH);
                        ImageIcon resizedIcon=new ImageIcon(image);
                        labelImage.setIcon(resizedIcon);
                    }
                    else
                    {
                        labelImage.setIcon(null);
                    }
                }
                else
                {
                    labelImage.setIcon(null);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Email not found", "Not found", JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchButtonActionPerformed

